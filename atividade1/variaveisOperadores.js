var numero1 = 10;
var numero2 = 5;


var soma = numero1 + numero2;
var subtracao = numero1 - numero2;
var multiplicacao = numero1 * numero2;
var divisao = numero1 / numero2;
var restoDivisao = numero1 % numero2; 

console.log(`Resultado da soma entre ${numero1} e ${numero2} = ${soma}`);
console.log(`Resultado da subtração entre ${numero1} e ${numero2} = ${subtracao}`);
console.log(`Resultado da multiplicação entre ${numero1} e ${numero2} = ${multiplicacao}`);
console.log(`Resultado da divisão entre ${numero1} e ${numero2} = ${divisao}`);
console.log(`Resultado do resto da divisão entre ${numero1} e ${numero2} = ${restoDivisao}`);