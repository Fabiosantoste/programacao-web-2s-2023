
var numA = 10;
var numB = 5;

var sumResult = numA + numB;
console.log(`Resultado da soma entre ${numA} e ${numB} = ${sumResult}`);

var subResult = numA - numB;
console.log(`Resultado da subtração entre ${numA} e ${numB} = ${subResult}`);


var mulResult = numA * numB;
console.log(`Resultado da multiplicação entre ${numA} e ${numB} = ${mulResult}`);

var divResult = numA / numB;
console.log(`Resultado da divisão entre ${numA} e ${numB} = ${divResult}`);
